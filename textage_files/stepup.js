pt_list=[];

function su_sort(key1, key2)
{
	return key1[3] - key2[3];
}

function make_pt_list(idx)
{
	pt_list[idx]=[];
	for (i=0; i<4; i++) {
		su_data[(idx-1)*4+i][1].sort(su_sort);
		pt_list[idx].push(su_data[(idx-1)*4+i]);
	}
}


